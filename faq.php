<h4>What is N&#257;ty&#257;rambha all about?</h4>
<p>N&#257;ty&#257;rambha, is a detailed and interactive Bharatanatyam practice app that enables practitioners and students, practice the basics of Bharatanatyam, a digital connect between the Guru and the shishya, the teacher and the student, thereby bridging classroom training under a teacher and personal home rehearsals.</p>
<br/>
<h4>Who is N&#257;ty&#257;rambha intended for?</h4>
<p>N&#257;ty&#257;rambha is an easy tool that helps beginners to learn and practice and a powerful aid for professionals and mature dancers to polish and perfect basic technique and grammar of the essential basics of Bharatanatyam, anytime, anywhere. N&#257;ty&#257;rambha is also a ready reference tool for young teachers of Bharatanatyam.</p>
<br/>
<h4>Can I learn Bharatanatyam from this website?</h4>
<p>N&#257;ty&#257;rambha strongly recommends learning from a teacher or a guru. N&#257;ty&#257;rambha compliments classroom training and provides an easy to use digital referencing to enable practice at home. N&#257;ty&#257;rambha aims to bring technology to Bharatanatyam, and is not meant to be a substitute for classroom training.</p>
<br/>

<h4>Which style of Bharatanatyam does this website follow?</h4>
<p>N&#257;ty&#257;rambha follows the pedagogy of the traditional Kalakshetra style of Bharatanatyam.</p>
<br/>

<h4>I am a Bharatanatyam teacher. How can I use this website?</h4>
<p>While N&#257;ty&#257;rambha aims to be a useful tool for students, it is a simple   tool for Bharatanatyam teachers in the following ways:<br/>
a. Serves as standard guidance for the basic coursework for your classes.<br/>
b. Allows you to create your own practice routines for your class that you could share with your students.<br/>
c. Helps students perfect the basics, even as it lets the teacher let you focus on creativity.</p>
<br/>
<h4>My child is a student of Bharatanatyam. Will N&#257;ty&#257;rambha be useful to me?</h4>
<p>If your child goes to a Bharatanatyam class and struggles to practice at home or doesn’t practice at all, you know the answer already.
With detailed guidance and readymade routines, N&#257;ty&#257;rambha is designed to make practice at home easy and fun. As a parent, you can easily monitor your child's practice sessions, even if you don’t know the technical details of Bharatanatyam.</p>
<br/>

<h4>Do I need to be connected to the internet to use N&#257;ty&#257;rambha?</h4>
<p>N&#257;ty&#257;rambha is a contemporary online Bharatanatyam practice application. We strive to update the content regularly and provide more value to our users.
Hence the answer is Yes, you need to be connected to the internet. We suggest you connect to a WiFi connection so that the videos can play without buffering issues. If you are using a low bandwidth connection, we suggest you switch off video and use only audio.</p>
<br/>
<h4>I don't want to see the videos all the time. Is there an option to just use audio?</h4>
<p>Absolutely. Here’s how you do it. Go to Menu -> Settings. Switch Video off. You can change this setting anytime.</p>
<br/>

<h4>I don't want to hear  the  instrumental music  all the time. Is there an option to  switch off the music?</h4>
<p>Absolutely. Here’s how you do it. Go to Menu -> Settings. Switch Audio  off. You can change this setting anytime.
The violin instrumentation will now be switched off, and you could continue practice with the basic mnemonic syllables and Mridangam rhythms.</p>
<br/>
<h4>How do I practice using N&#257;ty&#257;rambha?</h4>
<p>With N&#257;ty&#257;rambha, we aim to make Bharatanatyam practice at home easy and fun. Sign-up to our website and use the "Practice" option. You could either use one of the default practice routines that we have put together for you or create your own routine. Once you do this once, all you have to do when you practice is to sign-in and start your favourite routine and dance along. Don't forget to do the Namaskaram and warm up and cool down routine before and after your practice.</p>
<br/>

<h4>Which devices does Natyarambha run on?</h4>
<p>N&#257;ty&#257;rambha runs on a web browser. So it can run on pretty much any of your devices - laptops, tablets, mobiles. Use your favourite browser.
 We have done due diligence in testing across a variety of popular devices and browsers. But please feel free to report any issues that you might be facing with using N&#257;ty&#257;rambha on your device. We will fix it soon! Thanks for your understanding :).
Tested browsers - Chrome, Firefox, Safari and Internet Explorer.
Tested devices - iPad, iPad Mini, iPhone 6s, iPhone 5s, Popular Android phones (Samsung Galaxy series)</p>
<br/>

<h4>How do I create my own personalized routines?</h4>
<p>One of the promises with N&#257;ty&#257;rambha is that in addition to having ready  made practice routines that we have put together, you could create your own. To do so, go to 'Practice' and click on 'Create your own practice routine'. Give a name and a suitable description as you like, select which speeds you wish to add in the steps, choose the adavus and hit submit. Your routine is ready!</p>
<br/>

<h4>Once I create a routine, can I change it?</h4>
<p>Yes, you can! Click on the edit button on the routine and you could add / delete adavus from the routine. Don't forget to hit 'Submit' to save the changes.
If you think you are bored with this routine and want to create another one, you could go ahead and delete the whole routine  as well.</p>
<br/>

<h4>Can I share my routines with others?</h4>
<p>Absolutely. If you think you have created a practice routine that works well for you, there is a possibility that others could benefit from it too. Pick the routine that you wish to share and click on the 'Share' button. Copy the link that  pops up and share with others via email.
See 'What does one need to do to access a routine that is shared?'</p>
<br/>

<h4>What does one need to do to access a routine that is shared?</h4>
<p>Simply click on the link that you obtained for the routine. Sign in to N&#257;ty&#257;rambha if asked to do so. The routine should start playing now!</p>
<br/>

<h4>I am different not familiar with the steps in the videos. I have learnt them differently in my class. N&#257;ty&#257;rambha is not useful to me perhaps?</h4>
<p>Bharatanatyam teachers around the world have over the years adopted their own styles and versions of the basic steps. If you find a lot of deviation from what you are familiar with, our suggestion is to switch off the videos and just use audio as guidance. You may find the 'Learn more' section with detailed descriptions of the steps and the mnemonics useful to you.
Also read the answer to 'Which style of Bharatanatyam does this website follow?' for more details.</p>
<br/>

